var d = window.prompt("Vilken dag?");
switch (d){
    case "måndag":
        document.write("Kyckling");
        break;
    case "tisdag":
        document.write("Pannkaka");
        break;
    case "onsdag":
        document.write("Ärtsoppa");
        break;
    case "torsdag":
        document.write("Gryta");
        break;
    case "fredag":
        document.write("Kalv");
        break;
    case "lördag":
        document.write("Vegetariskt");
        break;
    case "söndag":
        document.write("Kålsoppa");
        break;
}